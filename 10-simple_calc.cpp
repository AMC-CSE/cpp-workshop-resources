#include <iostream>
using namespace std;

int main() {
    int x, y; // declaring variables x and y
    int sum; // declaring sum
    cout << "Type a number: "; // printing a message
    cin >> x; // storing input in x
    cout << "Type another number: "; //printing message
    cin >> y; // storing input in y
    sum = x + y; //performing addition and storing in sum
    cout << "Sum is: " << sum; // printing sum.
}