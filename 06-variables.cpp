// variables are containers for storing data values.

/*
Types of variables:

- int = stores integers. eg: 123, 456
- double = stores floating point numbers with decimals. eg: 19.99.
- char = store a single character, they are sorrounded by '' aka single quotes. eg: 'a'.
- string = stores text, wrapped in "" aka double quotes. eg: "Hello World!".
- bool = stores True or False.
*/

// Creating a Variable:

//type variable = value;

int myNum = 5;               // Integer (whole number without decimals)
double myFloatNum = 5.99;    // Floating point number (with decimals)
char myLetter = 'D';         // Character
string myText = "Hello";     // String (text)
bool myBoolean = true;       // Boolean (true or false)

// declaring multiple variables

int x = 5, y = 6, z = 50;
